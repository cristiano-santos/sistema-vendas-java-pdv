    private void carregarUsuario() {
        listaModelUsuario = controllerUsuario.retornarListaUsuarioController();
        DefaultTableModel modelo = (DefaultTableModel) jTableUsuario.getModel();
        modelo.setNumRows(0);
        int cont = listaModelUsuario.size();
        for (int i = 0; i < cont; i++) {
            modelo.addRow(new Object[]{
                listaModelUsuario.get(i).getIdUsuario(),
                listaModelUsuario.get(i).getUsuNome(),
                listaModelUsuario.get(i).getUsuLogin(),
                listaModelUsuario.get(i).getUsuSenha()
            });
        }
    }

    private void salvarUsuario() {
        modelUsuario.setUsuNome(this.jTextNome.getText());
        modelUsuario.setUsuLogin(this.jTextLogin.getText());
        modelUsuario.setUsuSenha(this.jTextSenha.getPassword().toString());

        if (controllerUsuario.salvarUsuarioController(modelUsuario) > 0) {
            JOptionPane.showMessageDialog(this, "Usuario Cadastrado com Sucesso!", "INFORMAÇÃO", JOptionPane.INFORMATION_MESSAGE);
            this.carregarUsuario();
            this.limparCampos();
            this.habilitarDesabilitarCampos(false);
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar usuario.", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void alterarUsuario() {
        modelUsuario.setUsuNome(this.jTextNome.getText());
        modelUsuario.setUsuLogin(this.jTextLogin.getText());
        modelUsuario.setUsuSenha(this.jTextSenha.getPassword().toString());

        if (controllerUsuario.alterarUsuarioController(modelUsuario)) {
            JOptionPane.showMessageDialog(this, "Usuario Alterado com Sucesso!", "INFORMAÇÃO", JOptionPane.INFORMATION_MESSAGE);
            this.carregarUsuario();
            this.limparCampos();
            this.habilitarDesabilitarCampos(false);
            jBtNovo.setEnabled(true);
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao alterar usuario.", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void habilitarDesabilitarCampos(boolean condicao) {
        jTextNome.setEnabled(condicao);
        jTextLogin.setEnabled(condicao);
        jTextSenha.setEnabled(condicao);
        jBtSalvar.setEnabled(condicao);
    }

    private void limparCampos() {
        jTextCodigo.setText("");
        jTextNome.setText("");
        jTextLogin.setText("");
        jTextSenha.setText("");
    }

