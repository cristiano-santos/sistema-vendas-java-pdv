    private void jBtExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtExcluirActionPerformed
        // TODO add your handling code here:
        try {
            int linha = jTableUsuario.getSelectedRow();
            int codigoUsuario = (int) jTableUsuario.getValueAt(linha, 0);

            if (controllerUsuario.excluirUsuarioController(codigoUsuario)) {
                JOptionPane.showMessageDialog(this, "Usuario excluído com Sucesso!", "INFORMAÇÃO", JOptionPane.INFORMATION_MESSAGE);
                this.carregarUsuario();
                this.limparCampos();
                this.habilitarDesabilitarCampos(false);
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao excluir usuario.", "ERRO", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Código inválido ou nenhum registro selecionado.", "ALERTA", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jBtExcluirActionPerformed

    private void jBtAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtAlterarActionPerformed
        // TODO add your handling code here:
        alterarSalvar = "alterar";
        this.habilitarDesabilitarCampos(true);
        jBtNovo.setEnabled(false);
        int linha = jTableUsuario.getSelectedRow();
        try {
            int codigoUsuario = (int) jTableUsuario.getValueAt(linha, 0);
            modelUsuario = controllerUsuario.retornarUsuarioController(codigoUsuario);
            this.jTextCodigo.setText(String.valueOf(modelUsuario.getIdUsuario()));
            this.jTextNome.setText(modelUsuario.getUsuNome());
            this.jTextLogin.setText(modelUsuario.getUsuLogin());
            this.jTextSenha.setText(String.valueOf(modelUsuario.getUsuSenha()));

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Código inválido ou nenhum registro selecionado.", "AVISO", JOptionPane.WARNING_MESSAGE);
            this.habilitarDesabilitarCampos(false);
            jBtNovo.setEnabled(true);
        }
    }//GEN-LAST:event_jBtAlterarActionPerformed

