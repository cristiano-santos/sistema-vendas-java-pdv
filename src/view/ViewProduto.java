    private void salvarProduto() {
        modelProduto.setProdNome(this.jTextNome.getText().toUpperCase());
        modelProduto.setProdValor(formatador.converterVirgulaParaPonto(this.jTextValor.getText()));
        modelProduto.setProdEstoque(Integer.parseInt(this.jTextEstoque.getText()));
        if (controllerProduto.salvarProdutoController(modelProduto) > 0) {
            JOptionPane.showMessageDialog(this, "Produto Cadastrado com Sucesso!", "INFORMAÇÃO", JOptionPane.INFORMATION_MESSAGE);
            this.carregarProdutos();
            this.limparCampos();
            this.habilitarDesabilitarCampos(false);
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao Cadastrar Produto.", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void alterarProduto() {
        modelProduto.setProdNome(this.jTextNome.getText());
        modelProduto.setProdValor(formatador.converterVirgulaParaPonto(this.jTextValor.getText()));
        modelProduto.setProdEstoque(Integer.parseInt(this.jTextEstoque.getText()));
        if (controllerProduto.alterarProdutoController(modelProduto)) {
            JOptionPane.showMessageDialog(this, "Produto Alterado com Sucesso!", "INFORMAÇÃO", JOptionPane.INFORMATION_MESSAGE);
            this.carregarProdutos();
            this.limparCampos();
            this.habilitarDesabilitarCampos(false);
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao alterar Produto.", "ERRO", JOptionPane.ERROR_MESSAGE);
        }

    }

    private void habilitarDesabilitarCampos(boolean condicao){
        jTextNome.setEnabled(condicao);
        jTextValor.setEnabled(condicao);
        jTextEstoque.setEnabled(condicao);
    }
    
    private void carregarProdutos(){
        listaModelProduto = controllerProduto.retornarListaProdutoController();
        DefaultTableModel modelo = (DefaultTableModel) jTbProduto.getModel();
        modelo.setNumRows(0);
        
        int cont = listaModelProduto.size();
        for (int i = 0; i < cont; i++) {
            modelo.addRow(new Object[]{
                listaModelProduto.get(i).getIdProduto(),
                listaModelProduto.get(i).getProdNome(),
                listaModelProduto.get(i).getProdValor(),
                listaModelProduto.get(i).getProdEstoque()
            });
        }
    }
    
    private void limparCampos(){
        jTextCodigo.setText("");
        jTextNome.setText("");
        jTextValor.setText("");
        jTextEstoque.setText("");
    }

