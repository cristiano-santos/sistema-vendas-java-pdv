    private void jBtAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtAlterarActionPerformed
        // TODO add your handling code here:
        alterarSalvar = "alterar";
        this.habilitarDesabilitarCampos(true);
        int linha = jTbProduto.getSelectedRow();
        try {
            int codigoProduto = (int) jTbProduto.getValueAt(linha, 0);
            modelProduto = controllerProduto.retornarProdutoController(codigoProduto);
            this.jTextCodigo.setText(String.valueOf(modelProduto.getIdProduto()));
            this.jTextNome.setText(modelProduto.getProdNome());
            this.jTextValor.setText(String.valueOf(modelProduto.getProdValor()));
            this.jTextEstoque.setText(String.valueOf(modelProduto.getProdEstoque()));

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Código inválido ou nenhum registro selecionado.", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
            this.habilitarDesabilitarCampos(false);
        }
    }//GEN-LAST:event_jBtAlterarActionPerformed

    private void jBtPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtPesquisarActionPerformed
        // TODO add your handling code here:
        DefaultTableModel modelo = (DefaultTableModel) this.jTbProduto.getModel();
        final TableRowSorter<TableModel> classificador = new TableRowSorter<>(modelo);
        this.jTbProduto.setRowSorter(classificador);
        String texto = jTextPesquisa.getText().toUpperCase();
        classificador.setRowFilter(RowFilter.regexFilter(texto, 1));
    }//GEN-LAST:event_jBtPesquisarActionPerformed

